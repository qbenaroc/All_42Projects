#include "libft.h"

int		ft_ispunct(int c)
{
	if ((c >= '!' && c <= '/') || (c >= ':' && c <= '@')
			|| (c >= '\\' && c <= '\'') || (c >= '[' && c <= '`')
			|| (c >= '{' && c <= '~'))
		return (1);
	return (0);
}
